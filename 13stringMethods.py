#upper()
name = "SouravMukherjee"
print(name.upper())


#lower()
name = "Heritage"
print(name.lower())

#Strip- Methods used for remove any white spaces before and after string.
name = "Sourav Mukherjee"
print(name.strip)

#rstrip - For remove any trailing characters
name = "Hello!!!"
print(name.rstrip("!"))

#replace - For replace all occurances if a string eith another string.
String = "Sourav Mukherjee"
print(String.replace("Sourav","Tatan"))


#split - For split a given string
String = "Sourav Mukherjee"
print(String.split(" "))

